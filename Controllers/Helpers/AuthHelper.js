module.exports = {
  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
  userLoggedIn(req, res, next) {
    if (req.isAuthenticated())
      return next();
    // no logged user
    res.redirect('/signin');
  }
  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
};

